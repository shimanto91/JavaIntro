package Java_intro;

public class Variables {
    public static void main(String[]args) {
      int num1 = 100;  //Takes 32 bit place regardless  of input value
      short num5 = 100;   //Takes shorter place
      String name = "Java Intro";
      char myChar = 'Z';
      float num2 = 3.7898f; //Type casting
      double num4 = 10.984674889583474834938; //For longer float
      boolean myStatus1 = true;
      boolean myStatus2 = false;

      System.out.println(num1);
      System.out.println(num5);
      System.out.println(name);
      System.out.println(myChar);
      System.out.println(num2);
      System.out.println(num4);
      System.out.println(myStatus1);
      System.out.println(myStatus2);
    }
}
