package Java_intro;

public class Operators {
    public static void main(String[] args) {
        int x = 10;
        int y = 5;

        int sum = x + y;
        int sub = x - y;
        int mult = x * y;
        int div = x / y;

        System.out.println(sum);
        System.out.println(sub);
        System.out.println("Multiplication is: " + mult); //Concatenation
        System.out.println("Division is: " + div);
    }
}
